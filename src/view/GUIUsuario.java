    private void abrirCadastrarDoador() {
        if (flagGUICadastrarDoador == false) {
            //Criando o objeto e estanciando a classe
            GUICadastrarSe gCD = new GUICadastrarSe();
            gCD.setTitle("Cadastrar Doador");

            //adicionando o objeto (janela) na área de trabalho
            jdpAreaDeTrabalho.add(gCD);

            //Setando a visibilidade como "true" para a janela aparecer para o usuário
            gCD.setVisible(true);

            //Setando o flagGUIClientes como verdadeiro, indicando que a janela foi aberta.
            flagGUICadastrarDoador = true;

            //Setando a janela na lista de ouvidores
            gCD.addInternalFrameListener(this);
        }
    }
    
