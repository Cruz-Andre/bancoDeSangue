    @Override
    public void internalFrameClosed(InternalFrameEvent ife) {
        if (ife.getInternalFrame() instanceof GUIManutencaoDoador) {
            flagGUIManutencaoDoador = false;
        } else if (ife.getInternalFrame() instanceof GUIManutencaoHospital) {
            flagGUIManutencaoHospital = false;
        } else if (ife.getInternalFrame() instanceof GUICadastrarHospitais) {
            flagGUICadastrarHospitais = false;
        } else if (ife.getInternalFrame() instanceof GUITabelaQPDPQ) {
            flagGUITabelaQPDPQ = false;
        } else if (ife.getInternalFrame()instanceof GUICadastrarSe) {
            flagGUICadastrarDoador = false;
        }

    }

